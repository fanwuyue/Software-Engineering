package top.womoe.model;

public class Hr {
    private Integer id;

    private String hrUsername;

    private String hrPassword;

    private String hrNumber;

    private String hrName;

    private String hrSex;

    private Integer hrSeniority;

    private String hrTelephone;

    private String hrToken;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getHrUsername() {
        return hrUsername;
    }

    public void setHrUsername(String hrUsername) {
        this.hrUsername = hrUsername;
    }

    public String getHrPassword() {
        return hrPassword;
    }

    public void setHrPassword(String hrPassword) {
        this.hrPassword = hrPassword;
    }

    public String getHrNumber() {
        return hrNumber;
    }

    public void setHrNumber(String hrNumber) {
        this.hrNumber = hrNumber;
    }

    public String getHrName() {
        return hrName;
    }

    public void setHrName(String hrName) {
        this.hrName = hrName;
    }

    public String getHrSex() {
        return hrSex;
    }

    public void setHrSex(String hrSex) {
        this.hrSex = hrSex;
    }

    public Integer getHrSeniority() {
        return hrSeniority;
    }

    public void setHrSeniority(Integer hrSeniority) {
        this.hrSeniority = hrSeniority;
    }

    public String getHrTelephone() {
        return hrTelephone;
    }

    public void setHrTelephone(String hrTelephone) {
        this.hrTelephone = hrTelephone;
    }

    public String getHrToken() {
        return hrToken;
    }

    public void setHrToken(String hrToken) {
        this.hrToken = hrToken;
    }
}